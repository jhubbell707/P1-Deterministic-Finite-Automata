package fa.dfa;

import fa.State;

/**
 * Extension of abstract class State.
 * 
 * @author jimmyhubbell
 * @version Fall 2023
 */
public class DFAState extends State {

    public DFAState(String name) {
        super(name);
    }

    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public String toString() {
        return super.toString();
    }
    
}
